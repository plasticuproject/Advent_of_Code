def part_two(slope):
    """Part 2 solution"""
    x = slope[0]
    y = slope[1]
    trees = 0
    line_len = len(LAND[0])
    for line in range(len(LAND) - 1):
        if y > len(LAND):
            return (trees)
        while x >= line_len:
            x -= line_len
        if LAND[y][x] == '#':
            trees += 1
        x += slope[0]
        y += slope[1]
    return trees


# Slopes
