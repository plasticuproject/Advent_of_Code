# Slope right 3, down 1
def part_one():
    """Part 1 solution"""
    x = 3
    y = 1
    trees = 0
    line_len = len(LAND[0])
    for line in range(len(LAND) - 1):
        while x >= line_len:
            x -= line_len
        if LAND[y][x] == '#':
            trees += 1
        x += 3
        y += 1
    print(f'Solution 1: {trees}')


